// Capturar 2 numeros e fazer as operações matemáticas de soma, subtração, multiplicação, divisão e resto da divisão

// Para cada operação, mostrar um alerta com o resultado

let firstNumber = Number(prompt('Digite o primeiro número'))
let secondNumber = Number(prompt('Digite o segundo número'))

let sumResult = firstNumber + secondNumber
let subResult = firstNumber - secondNumber
let multiResult = firstNumber * secondNumber
let divisionResult = firstNumber / secondNumber
let restResult = firstNumber % secondNumber

alert("A soma dos números é: " + sumResult)
alert("A subtração dos números é: " + subResult)
alert("A multiplicação dos números é: " + multiResult)
alert("A divisão dos números é: " + divisionResult)
alert("O resto da divisão dos números é: " + restResult)




